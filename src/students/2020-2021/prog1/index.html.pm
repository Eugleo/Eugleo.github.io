◊section{Informace o předmětu}

Podmínkou k úspěšném splnění předmětu je vypracování povinných domácích úkolů a získání alespoň 60 bodů ze 100 v závěrečné zkoušce.

Studenti mohou navíc už během roku získat body, které se jim na konci připočítají ke zkoušce, a to konkrétně vypracováváním bonusových domácích úkolů a úspěšným plněním průběžných písemných testů. Každému studentovi bude tímto způsobem ke zkoušce převedeno až 50 bodů.

◊info-box{S přihlédnutím ke známkovým hranicím níže je zřejmé, že i ten nejaktivnější student musí ze závěrečné zkoušky vždy získat alespoň 10 bodů, aby prošel ročníkem.

Na druhou stranu i student, který nedělal dobrovolné domácí úlohy a moc se mu nedařilo v průběžných testech může nakonec dostat jedničku, pokud hezky zvládne závěrečnou zkoušku.}

Konkrétní bodové hranice a jim odpovídající známky:

Studenti budou v během průběžně známkování přes systém Bakalář, ale tyto budou spíše orientačního charakteru. Zde na stránkách si bude moct každý student v průběhu roku kontrolovat, kolik bodů má a kolik ještě může (nebo musí) získat.

◊subsection{Podoba zkoušky a průběžných testů}

Zkouška se skládá z písemné a ústní části. Písemná část zkoušky je velmi podobná průběžným testům: na začátku hodiny je zadána jedna úloha, na jejíž naprogramování budou mít studenti v případě testu 60 minut, a v případě zkoušky 90 minut.

Ústní část zkoušky bude krátká diskuze s vyučujícím o předešlé písemné části a o tématech probraných během roku — všechny důležité informace z těchto témat budou v oficiálních zápiscích z přednášek zde na stránkách.

